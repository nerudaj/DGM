#include "GasParticleSystem.hpp"

dgm::ResourceManager getInitializedResourceMgr();
void exportLevel();

int main(int argc, char *argv[]) {
	freopen("stdout.txt", "w", stdout);

	//exportLevel();

	dgm::Window window;
	window.open({ 1280, 720 }, "Advanced particle systems", false);

	dgm::Time time;
	dgm::ResourceManager resmgr = getInitializedResourceMgr();

	Level level(resmgr.get<sf::Texture>("tileset.png"));
	level.loadFromFile(rootDir + "/11_advanced_particle_system.lvd");

	SandSystem sandSystem;
	if (!sandSystem.init(250)) return 1;
	sandSystem.gravity = { 0.f, 100.f };
	sandSystem.emitterPosition = { 150.f, 40.f };
	sandSystem.level = &(level.getMesh());
	time.reset();

	GasSystem gasSystem;
	gasSystem.emitterPosition = sf::Vector2f(16.f, 14.f) * float(TILE_SIZE);
	gasSystem.level = &(level.getMesh());
	gasSystem.clip = dgm::Clip({ 16, 16 }, { 0, 0, 48, 16 });
	gasSystem.setTexture(resmgr.get<sf::Texture>("gas.png"));
	if (!gasSystem.init(250)) return 2;

	bool logicActive = false;
	sf::Event event;
	while (window.isOpen()) {
		while (window.pollEvent(event)) {
			if (event.type == sf::Event::Closed) {
				window.close();
			}
			else if (event.type == sf::Event::KeyPressed) {
				// If space is pressed, remove some blocks from the level so the sand can fall
				if (event.key.code == sf::Keyboard::Space) {
					level.changeTileToVoid(4, 3);
					level.changeTileToVoid(5, 6);
					level.changeTileToVoid(4, 9);
					level.changeTileToVoid(5, 12);
					level.changeTileToVoid(4, 15);
				}
				else if (event.key.code == sf::Keyboard::P) {
					logicActive = true;
				}
			}
		}

		/* LOGIC */
		if (logicActive) {
			std::thread t1(&SandSystem::update, &sandSystem, time);
			std::thread t2(&GasSystem::update, &gasSystem, time);
			t1.join();
			t2.join();
		}
		time.reset();

		/* RENDER */
		window.beginDraw();

		level.draw(window);
		sandSystem.draw(window);
		gasSystem.draw(window);

		window.endDraw();
	}

	return 0;
}

dgm::ResourceManager getInitializedResourceMgr() {
	dgm::ResourceManager resmgr;
	resmgr.setPedantic(false);
	resmgr.loadResourceDir<sf::Texture>(rootDir);
	return resmgr;
}

